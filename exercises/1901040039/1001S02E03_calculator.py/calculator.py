number_1 = float(input("Please input the first number:"))

number_2 = float(input("Please input the second number:"))

caculation = input ("Please enter operator: ")

if caculation == "+":

        print(number_1+number_2)

elif caculation =="-":
                    
        print(number_1-number_2)

elif caculation =="*":

        print(number_1*number_2)

elif caculation =="/":

        print(number_1 / number_2)
        
else:

        print("wrong type")

        
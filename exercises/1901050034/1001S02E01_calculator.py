num1 = float(input('please enter one number:'))
num2 = float(input('please enter another number:'))
op = input('please enter a operational symbol:')
if op == '+':
    print(num1,'+',num2,'=',num1+num2)
if op == '-':
    print(num1,'-',num2,'=',num1-num2)
if op == '*':
    print(num1,'*',num2,'=',num1*num2)
if op == '/':
    print(num1,'/',num2,'=',num1 / num2)
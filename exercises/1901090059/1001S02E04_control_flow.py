###################################################
# 输出九九乘法表
###################################################

for i in range(1, 10):
    for j in range(1, 10):
        if i >= j:
            print(i,'*',j,'=',i*j,sep=' ', end='\t')
    print()

print('---------------------------------------------------------------------------------------------------------------------------')

# 输出奇数行的乘法表，采用While语句实现
###################################################
n = 1
while n <= 9 :
    m = 1
    while m <= n:
        print(n,'*',m,'=',n*m,sep=' ', end='\t')
        m = m +1
    print()
    n = n + 2

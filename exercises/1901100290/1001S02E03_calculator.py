a = float(input('Please enter a number: '))
option = input('Please enter a operator: ')
b = float(input('Please enter another number: '))
if option == '+':
    print("result: ",a+b)
if option == '-':
    print("result: ",a-b)
if option == '*':
    print("result: ",a*b)
if option == '/':
    print("result: ",a/b)



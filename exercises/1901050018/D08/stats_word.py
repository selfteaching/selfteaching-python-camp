def stats_text_en(text):                       #以降序方式统计英文词频
    if type(text)!=str:
        raise ValueError('请输入非字符串')
    import re
    a=re.sub('[^a-zA-Z]',' ',text)
    b=a.split()
    from collections import Counter
    c=Counter(b)
    return c

def stats_text_cn(text):                       #以降序方式统计中文词频
    if type(text)!=str:
        raise ValueError('请输入非字符串')
    d=text.replace('，','').replace('。','').replace('\n','')
    import re
    e=re.sub("[A-Za-z0-9\!\%\[\]\,\。,\.,\ ,\-,\',\*,\”,\’,\“,\：,\！\？\；,\（,\）]", "", d)
    f=list(e)
    from collections import Counter
    g=Counter(f)
    return g

def stats_text(text):
    if type(text)!=str:
        raise ValueError('请输入非字符串')
    return stats_text_en(text),stats_text_cn(text)
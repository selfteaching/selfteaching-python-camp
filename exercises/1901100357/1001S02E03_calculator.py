def add(x, y):
    print("adding {} and {}".format(x, y))
    return x + y

    
def subtract(x, y):
    print("subtracting {} and {}".format(x, y))
    return x - y


def multipy(x, y):
    print("multipying {} and {}".format(x, y))
    return x * y


def divide(x, y):
    print("dividing {} and {}".format(x, y))
    return x / y

a = add(1, 2)
s = subtract(2, 3)
m = multipy(3, 4)
d = divide(18, 3)

print(a)
print(s)
print(m)
print(d)